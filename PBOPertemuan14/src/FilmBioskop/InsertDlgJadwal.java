    private void jBtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSaveActionPerformed
        if (jTfIdJadwal.getText().equals("") || CbJudul.getSelectedIndex() == 0
                || CbStudio.getSelectedIndex() == 0 || jTfTanggal.getText().equals("")
                || jTfWaktu.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Isi semua data terlebih dahulu!");
            return;
        }

        String idJadwal = jTfIdJadwal.getText();
        String judulTerpilih = CbJudul.getSelectedItem().toString();
        String namaStudioTerpilih = CbStudio.getSelectedItem().toString();
        String tanggalStr = jTfTanggal.getText();
        String waktuStr = jTfWaktu.getText();

        try {
            // Parsing tanggal dan waktu
            java.sql.Date tanggal = java.sql.Date.valueOf(tanggalStr); // format: yyyy-MM-dd
            java.sql.Time waktu = java.sql.Time.valueOf(waktuStr);     // format: HH:mm:ss

            // Buat koneksi persistence
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBO_Pertemuan12PU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();

            // Ambil entitas Film berdasarkan judul (setiap judul unik)
            Query qFilm = em.createQuery("SELECT f FROM Film f WHERE f.judul = :judul");
            qFilm.setParameter("judul", judulTerpilih);
            Film film;
            try {
                film = (Film) qFilm.getSingleResult();
            } catch (NoResultException e) {
                JOptionPane.showMessageDialog(null, "Film tidak ditemukan di database!", "Error", JOptionPane.ERROR_MESSAGE);
                em.close();
                emf.close();
                return;
            }

            // Ambil Studio berdasarkan nama
            Query qStudio = em.createQuery("SELECT s FROM Studio s WHERE s.namaStudio = :nama");
            qStudio.setParameter("nama", namaStudioTerpilih);
            Studio studio;
            try {
                studio = (Studio) qStudio.getSingleResult();
            } catch (NoResultException e) {
                JOptionPane.showMessageDialog(null, "Studio tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
                em.close();
                emf.close();
                return;
            }

            // ðŸ”¹ Validasi 1 â€” studio + tanggal + waktu tidak boleh sama
            Query q1 = em.createQuery(
                    "SELECT COUNT(j) FROM JadwalTayang j WHERE j.idStudio = :studio AND j.tanggalTayang = :tanggal AND j.waktuTayang = :waktu"
            );
            q1.setParameter("studio", studio.getIdStudio());
            q1.setParameter("tanggal", tanggal);
            q1.setParameter("waktu", waktu);
            long count1 = (long) q1.getSingleResult();

            if (count1 > 0) {
                JOptionPane.showMessageDialog(null,
                        "Studio ini sudah digunakan pada tanggal dan waktu tersebut!",
                        "Jadwal Bentrok", JOptionPane.WARNING_MESSAGE);
                em.close();
                emf.close();
                return;
            }

            // ðŸ”¹ Validasi 2 â€” film + studio + tanggal + waktu tidak boleh sama
            Query q2 = em.createQuery(
                    "SELECT COUNT(j) FROM JadwalTayang j WHERE j.idFilm = :film AND j.idStudio = :studio AND j.tanggalTayang = :tanggal AND j.waktuTayang = :waktu"
            );
            q2.setParameter("film", film);
            q2.setParameter("studio", studio.getIdStudio());
            q2.setParameter("tanggal", tanggal);
            q2.setParameter("waktu", waktu);
            long count2 = (long) q2.getSingleResult();

            if (count2 > 0) {
                JOptionPane.showMessageDialog(null,
                        "Film ini sudah dijadwalkan di studio dan waktu tersebut!",
                        "Jadwal Duplikat", JOptionPane.WARNING_MESSAGE);
                em.close();
                emf.close();
                return;
            }

            // ðŸ”¹ Validasi 3 â€” idJadwal tidak boleh duplikat (Primary Key)
            JadwalTayang existing = em.find(JadwalTayang.class, idJadwal);
            if (existing != null) {
                JOptionPane.showMessageDialog(null,
                        "ID Jadwal sudah digunakan! Gunakan ID lain.",
                        "Duplikat ID Jadwal", JOptionPane.WARNING_MESSAGE);
                em.close();
                emf.close();
                return;
            }

            // Buat object JadwalTayang
            JadwalTayang jadwal = new JadwalTayang();
            jadwal.setIdJadwal(idJadwal);
            jadwal.setIdFilm(film);         // relasi FK ke entitas Film
            jadwal.setIdStudio(studio.getIdStudio());
            jadwal.setTanggalTayang(tanggal);
            jadwal.setWaktuTayang(waktu);

            // Simpan ke database
            em.persist(jadwal);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(null, "Data jadwal berhasil disimpan!");

            // Bersihkan field input
            bersih();

            // Refresh tabel di JFrame (pastikan ada method showTableJadwal di JFrame)
            if (utama != null) {
                utama.showTableJadwal();
            }

            em.close();
            emf.close();

        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, "Format tanggal harus yyyy-MM-dd dan waktu HH:mm:ss!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menyimpan data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jBtnSaveActionPerformed

