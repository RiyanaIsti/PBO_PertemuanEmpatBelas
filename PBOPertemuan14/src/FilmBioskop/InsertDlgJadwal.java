    private void loadJadwal() {
        try {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBO_Pertemuan12PU");
            EntityManager em = emf.createEntityManager();

            Query q = em.createQuery("SELECT f FROM Film f ORDER BY f.judul");
            List<Film> list = q.getResultList();

            CbJudul.removeAllItems();
            CbJudul.addItem("-- Pilih Judul Film --");

            for (Film f : list) {
                CbJudul.addItem(f.getJudul());
            }

            em.close();
            emf.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengambil data judul: " + e.getMessage());
        }
    }

    private void loadStudio() {
        try {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBO_Pertemuan12PU");
            EntityManager em = emf.createEntityManager();

            // Ambil semua Studio, urut berdasarkan namaStudio
            Query q = em.createQuery("SELECT s FROM Studio s ORDER BY s.namaStudio");
            List<Studio> list = q.getResultList();

            CbStudio.removeAllItems();
            CbStudio.addItem("-- Pilih Studio --"); // item default

            for (Studio s : list) {
                CbStudio.addItem(s.getNamaStudio()); // tampilkan nama studio
            }

            em.close();
            emf.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengambil data studio: " + e.getMessage());
        }
    }

